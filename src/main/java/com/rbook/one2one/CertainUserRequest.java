package com.rbook.one2one;

public class CertainUserRequest { // browser certain user debt
	private String user;

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	@Override
	public String toString() {
		return "CertainUserRequest [user=" + user + "]";
	}

}
